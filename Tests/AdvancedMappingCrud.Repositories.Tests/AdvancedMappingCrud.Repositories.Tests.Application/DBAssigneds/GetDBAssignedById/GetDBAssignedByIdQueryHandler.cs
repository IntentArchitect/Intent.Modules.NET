using System;
using System.Threading;
using System.Threading.Tasks;
using AdvancedMappingCrud.Repositories.Tests.Domain.Common.Exceptions;
using AdvancedMappingCrud.Repositories.Tests.Domain.Repositories.ExplicitAutoGenerated;
using AutoMapper;
using Intent.RoslynWeaver.Attributes;
using MediatR;

[assembly: DefaultIntentManaged(Mode.Fully)]
[assembly: IntentTemplate("Intent.Application.MediatR.QueryHandler", Version = "1.0")]

namespace AdvancedMappingCrud.Repositories.Tests.Application.DBAssigneds.GetDBAssignedById
{
    [IntentManaged(Mode.Merge, Signature = Mode.Fully)]
    public class GetDBAssignedByIdQueryHandler : IRequestHandler<GetDBAssignedByIdQuery, DBAssignedDto>
    {
        private readonly IDBAssignedRepository _dBAssignedRepository;
        private readonly IMapper _mapper;

        [IntentManaged(Mode.Merge)]
        public GetDBAssignedByIdQueryHandler(IDBAssignedRepository dBAssignedRepository, IMapper mapper)
        {
            _dBAssignedRepository = dBAssignedRepository;
            _mapper = mapper;
        }

        [IntentManaged(Mode.Fully, Body = Mode.Fully)]
        public async Task<DBAssignedDto> Handle(GetDBAssignedByIdQuery request, CancellationToken cancellationToken)
        {
            var dBAssigned = await _dBAssignedRepository.FindByIdAsync(request.Id, cancellationToken);
            if (dBAssigned is null)
            {
                throw new NotFoundException($"Could not find DBAssigned '{request.Id}'");
            }
            return dBAssigned.MapToDBAssignedDto(_mapper);
        }
    }
}