using CleanArchitecture.Comprehensive.Domain.Entities.DefaultDiagram;
using Intent.RoslynWeaver.Attributes;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;

[assembly: DefaultIntentManaged(Mode.Fully)]
[assembly: IntentTemplate("Intent.EntityFrameworkCore.EntityTypeConfiguration", Version = "1.0")]

namespace CleanArchitecture.Comprehensive.Infrastructure.Persistence.Configurations.DefaultDiagram
{
    public class ClassWithDefaultConfiguration : IEntityTypeConfiguration<ClassWithDefault>
    {
        public void Configure(EntityTypeBuilder<ClassWithDefault> builder)
        {
            builder.HasKey(x => x.Id);

            builder.Property(x => x.Name)
                .IsRequired();

            builder.Ignore(e => e.DomainEvents);
        }
    }
}