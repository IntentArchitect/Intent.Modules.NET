using System;
using System.Collections.Generic;
using System.Threading;
using System.Threading.Tasks;
using AdvancedMappingCrud.Repositories.Tests.Domain.Repositories.ExplicitAutoGenerated;
using AutoMapper;
using Intent.RoslynWeaver.Attributes;
using MediatR;

[assembly: DefaultIntentManaged(Mode.Fully)]
[assembly: IntentTemplate("Intent.Application.MediatR.QueryHandler", Version = "1.0")]

namespace AdvancedMappingCrud.Repositories.Tests.Application.DBAssigneds.GetDBAssigneds
{
    [IntentManaged(Mode.Merge, Signature = Mode.Fully)]
    public class GetDBAssignedsQueryHandler : IRequestHandler<GetDBAssignedsQuery, List<DBAssignedDto>>
    {
        private readonly IDBAssignedRepository _dBAssignedRepository;
        private readonly IMapper _mapper;

        [IntentManaged(Mode.Merge)]
        public GetDBAssignedsQueryHandler(IDBAssignedRepository dBAssignedRepository, IMapper mapper)
        {
            _dBAssignedRepository = dBAssignedRepository;
            _mapper = mapper;
        }

        [IntentManaged(Mode.Fully, Body = Mode.Fully)]
        public async Task<List<DBAssignedDto>> Handle(GetDBAssignedsQuery request, CancellationToken cancellationToken)
        {
            var dBAssigneds = await _dBAssignedRepository.FindAllAsync(cancellationToken);
            return dBAssigneds.MapToDBAssignedDtoList(_mapper);
        }
    }
}