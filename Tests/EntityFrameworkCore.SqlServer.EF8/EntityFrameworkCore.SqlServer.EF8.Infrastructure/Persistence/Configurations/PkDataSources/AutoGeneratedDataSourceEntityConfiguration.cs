using EntityFrameworkCore.SqlServer.EF8.Domain.Entities.PkDataSources;
using Intent.RoslynWeaver.Attributes;
using Microsoft.EntityFrameworkCore;
using Microsoft.EntityFrameworkCore.Metadata.Builders;

[assembly: DefaultIntentManaged(Mode.Fully)]
[assembly: IntentTemplate("Intent.EntityFrameworkCore.EntityTypeConfiguration", Version = "1.0")]

namespace EntityFrameworkCore.SqlServer.EF8.Infrastructure.Persistence.Configurations.PkDataSources
{
    public class AutoGeneratedDataSourceEntityConfiguration : IEntityTypeConfiguration<AutoGeneratedDataSourceEntity>
    {
        public void Configure(EntityTypeBuilder<AutoGeneratedDataSourceEntity> builder)
        {
            builder.HasKey(x => x.Id);

            builder.Property(x => x.Id)
                .ValueGeneratedOnAdd();

            builder.Property(x => x.FieldValue)
                .IsRequired();
        }
    }
}