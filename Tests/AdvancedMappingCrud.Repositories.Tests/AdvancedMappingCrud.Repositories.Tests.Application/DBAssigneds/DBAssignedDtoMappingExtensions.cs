using System.Collections.Generic;
using System.Linq;
using AdvancedMappingCrud.Repositories.Tests.Domain.Entities.ExplicitAutoGenerated;
using AutoMapper;
using Intent.RoslynWeaver.Attributes;

[assembly: DefaultIntentManaged(Mode.Fully)]
[assembly: IntentTemplate("Intent.Application.Dtos.AutoMapper.MappingExtensions", Version = "1.0")]

namespace AdvancedMappingCrud.Repositories.Tests.Application.DBAssigneds
{
    public static class DBAssignedDtoMappingExtensions
    {
        public static DBAssignedDto MapToDBAssignedDto(this DBAssigned projectFrom, IMapper mapper)
            => mapper.Map<DBAssignedDto>(projectFrom);

        public static List<DBAssignedDto> MapToDBAssignedDtoList(this IEnumerable<DBAssigned> projectFrom, IMapper mapper)
            => projectFrom.Select(x => x.MapToDBAssignedDto(mapper)).ToList();
    }
}